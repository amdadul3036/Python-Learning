from math import*


# Number
print(5)

# String
print("5")

# Reminder
print(10%3)

# Convert a number into a string
print(str(10))

# Absolute value
print(abs(-19))


# Power of a number
print(pow(2,5))
# It will print the value of 2^5



# Get the MAXIMUM number or LARGEST number
print(max(3,75,34,78,97,4,678))


# Get the MINIMUM number or SMALLEST number
print(min(3,75,34,78,97,4,678))


# Get the round value of a float number
print(round(35424.4454))
print(round(35424.6454))



# Floor a number
print(floor(34.245))

# Ceil a number
print(ceil(34.245))


# Root squre a number
print(sqrt(5))



# Complex Type Number
comp1 = 3 + 4j 
comp2 = 5 - 5j
comp3 = 5.6 + 8j

# In python j = sqrt(-1); you cannot use i as it is. 
print(type(comp1))
print(type(comp2))
print(type(comp3))

# Convert integer to float 
a = 5 
b = float(a)
print(b)
# OUTPUT
# 5.0
print(type(b))
# OUTPUT
# <class 'float'>



# Convert float into integer.
s = 4.566
print(type(s))
# OUTPUT
# <class 'float'>
t = int(s)
print(t)
# OUTPUT
# 4
print(type(t))
# OUTPUT
# <class 'int'>


# convert in Complex 
p = 3
q = -4
r = complex(p,q)
print(r)
# OUTPUT
# (3-4j)
print(type(r))
# OUTPUT
# <class 'complex'>
