# Tuple is like list ; It's index numbering is exactly like list. But we cannot modify or update a tuple.

coordinate = (4,5)
# This is a tuple and it use paranthesis () 
print(coordinate)
# It prints (4,5)
print(coordinate[0])
# It will print 0 index element of the tuple that is 4. 



